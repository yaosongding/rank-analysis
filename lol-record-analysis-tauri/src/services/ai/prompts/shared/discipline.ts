/**
 * 指标名纪律：区分英雄版本数据与玩家个人数据，禁用材料外指标名。
 * @param playerDataDesc - 该场景下"玩家个人数据"在材料中的呈现方式描述
 *   （如选人期是「我方玩家"常用"括号里的胜率/场次」，对局中是「玩家画像里的胜率/KDA/场次」）
 */
export function metricNameRule(playerDataDesc: string): string {
  return (
    `材料中的每个数字都有明确的指标名：敌方英雄的"胜率"是 OP.GG 版本胜率；${playerDataDesc}是该玩家个人近期数据，不是英雄的版本数据。` +
    '禁止使用材料中不存在的指标名（如"出场率""ban率"——材料未提供），禁止把玩家个人数据说成英雄版本数据。'
  )
}

